package org.simpel.pumpingUnits.controller.installationsUtilsModel;

public class InstallationPointRequest {
    private Double x;
    private Double y;
    private String type;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }
}
